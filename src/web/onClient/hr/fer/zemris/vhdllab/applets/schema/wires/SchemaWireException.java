package hr.fer.zemris.vhdllab.applets.schema.wires;

public class SchemaWireException extends Exception {

	public SchemaWireException() {
		super();
	}

	public SchemaWireException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public SchemaWireException(String arg0) {
		super(arg0);
	}

	public SchemaWireException(Throwable arg0) {
		super(arg0);
	}

}
