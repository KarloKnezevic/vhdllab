package hr.fer.zemris.vhdllab.applets.schema.components;

public class ComponentFactoryException extends Exception {

	public ComponentFactoryException() {
		super();
	}

	public ComponentFactoryException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public ComponentFactoryException(String arg0) {
		super(arg0);
	}

	public ComponentFactoryException(Throwable arg0) {
		super(arg0);
	}
	
}
