package hr.fer.zemris.vhdllab.api.vhdl;

/**
 * Describes a name of a port type in <code>ENTITY</code> block of VHDL code.
 * 
 * @author Miro Bezjak
 * @version 1.0
 * @since vhdllab2
 */
public enum TypeName {
	/**
	 * A std_logic port type name.
	 */
	STD_LOGIC,
	/**
	 * A std_logic_vector port type name.
	 */
	STD_LOGIC_VECTOR;

}
