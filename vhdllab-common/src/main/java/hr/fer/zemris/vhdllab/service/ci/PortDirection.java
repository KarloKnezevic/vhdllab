package hr.fer.zemris.vhdllab.service.ci;

/**
 * Describes a direction of a port in <code>ENTITY</code> block of VHDL code.
 * 
 * @author Miro Bezjak
 * @version 1.0
 * @since vhdllab2
 */
public enum PortDirection {
    /**
     * An IN direction of a port.
     */
    IN,
    /**
     * An OUT direction of a port.
     */
    OUT;
}
